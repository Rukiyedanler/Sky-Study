export const getRandomCityFact = (cityName: string): string => {
  const cleanName = cityName.split(' (')[0].trim().toUpperCase(); // "İSTANBUL (SAW)" -> "İSTANBUL"
  const facts = CITY_FACTS[cleanName];
  
  if (!facts || facts.length === 0) {
    return 'Bu güzel şehir hakkında yeni hikayeler keşfetmek için odaklanmaya devam edin!';
  }
  
  // Rastgele bir bilgi seç
  const randomIndex = Math.floor(Math.random() * facts.length);
  return facts[randomIndex];
};
